export const translations: Record<Language, typeof englishTranslations> = {
  en: englishTranslations,
  hi: hindiTranslations,
};

export const useTranslation = (lang: Language = 'en') => {
  // Validate language input
  const validLang: Language = (lang === 'en' || lang === 'hi') ? lang : 'en';
  
  // Get translations with fallback
  const t = translations[validLang] || translations['en'];
  
  // Return translations with safe getter that always returns a string
  return new Proxy(t, {
    get(target, prop) {
      const value = target[prop as keyof typeof target];
      // If translation is missing, return the key or English fallback
      if (value === undefined || value === null) {
        const englishValue = translations['en'][prop as keyof typeof translations['en']];
        return englishValue || String(prop);
      }
      return value;
    }
  });
  // Return translation for the language, fallback to English if not available
  return translations[lang] || translations['en'];
};
