import type { TranslationCache } from './translationsData';
export type { TranslationCache } from './translationsData';

type TranslationContextValue = { localeCache: TranslationCache; language: Language; isLocaleLoading: boolean };

export const useTranslation = (lang: Language = 'en') => {
  const ctx = useContext(TranslationContext) as TranslationContextValue | null;
  const isStatic = lang === 'en' || lang === 'hi';
  const staticT = isStatic ? (translations[lang as 'en' | 'hi'] || translations['en']) : translations['en'];
  const localeT = ctx && !isStatic ? { ...staticT, ...(ctx.localeCache[lang] || {}) } : staticT;

  return new Proxy(localeT, {
    get(target, prop) {
      const value = target[prop as keyof typeof target];
      if (value === undefined || value === null) {
        const englishValue = translations['en'][prop as keyof typeof translations['en']];
        return englishValue || String(prop);
      }
      return value;
    }
  });
};
